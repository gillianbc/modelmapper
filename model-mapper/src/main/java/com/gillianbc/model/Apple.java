package com.gillianbc.model;

public class Apple {
	private String skinType;
	private boolean sweet;
	private int pipsCount;
	private int producerNumber;
	private Tree tree;
	
	
	
	
	public String getSkinType() {
		return skinType;
	}
	public void setSkinType(String skinType) {
		this.skinType = skinType;
	}
	public boolean isSweet() {
		return sweet;
	}
	public void setSweet(boolean sweet) {
		this.sweet = sweet;
	}
	public int getPipsCount() {
		return pipsCount;
	}
	public void setPipsCount(int pipsCount) {
		this.pipsCount = pipsCount;
	}
	public int getProducerNumber() {
		return producerNumber;
	}
	public void setProducerNumber(int producerNumber) {
		this.producerNumber = producerNumber;
	}
	public Tree getTree() {
		return tree;
	}
	public void setTree(Tree tree) {
		this.tree = tree;
	}
	

}
