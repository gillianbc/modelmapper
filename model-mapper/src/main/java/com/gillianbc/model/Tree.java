package com.gillianbc.model;


public class Tree {
	private String treeName;

	public String getTreeName() {
		return treeName;
	}

	public void setTreeName(String treeName) {
		this.treeName = treeName;
	}

	@Override
	public String toString() {
		return "Tree [treeName=" + treeName + "]";
	}
	
	
	
}
